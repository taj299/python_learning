grade = float(input("enter your number grade:"))

if grade >= 90: 
  print("Your letter grade is an A.") 
elif grade >= 80: 
  print("Your letter grade is a B.") 
elif grade >= 70: 
  print("Your letter grade is a C.") 
elif grade >= 60: 
  print("Your letter grade is a D.") 
else: 
  print("Your letter grade is an F.")